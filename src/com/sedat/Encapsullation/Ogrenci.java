package com.sedat.Encapsullation;

public class Ogrenci {
	
	private String ad;
	private int yas;
	
	public Ogrenci(String ad,int yas) {
		this.ad=ad;
		this.yas=yas;
		
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public int getYas() {
		return yas;
	}

	public void setYas(int yas) {
		this.yas = yas;
	}
	
	

}
